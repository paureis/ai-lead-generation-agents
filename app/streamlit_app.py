def _safe_filename(value: str) -> str:
    cleaned = "".join(ch if ch.isalnum() or ch in {"-", "_"} else "_" for ch in value.lower())
    cleaned = "_".join(part for part in cleaned.split("_") if part)
    return cleaned[:80] or "lead_report"


