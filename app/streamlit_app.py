@st.cache_data(show_spinner=False)
def geocode_address_with_fallback(address: str, city: str = "", state: str = "FL"):
    if not address or not str(address).strip():
        return None, None, "missing"

    raw_address = str(address).strip()
    candidates = [raw_address]

    simplified = raw_address
    for token in ["STE", "SUITE", "UNIT", "#"]:
        if token in simplified.upper():
            upper = simplified.upper()
            idx = upper.find(token)
            if idx != -1:
                simplified = simplified[:idx].strip().rstrip(",")
                break
    if simplified and simplified != raw_address:
        candidates.append(simplified)

    if city:
        candidates.append(f"{city}, {state}")

    for i, candidate in enumerate(candidates[:2]):
        try:
            location = geolocator.geocode(candidate, timeout=2)
            if location:
                if i == 0:
                    return location.latitude, location.longitude, "exact"
                if i == 1:
                    return location.latitude, location.longitude, "simplified"
                return location.latitude, location.longitude, "city_fallback"
        except Exception:
            continue

    return None, None, "failed"

@st.cache_data(show_spinner=False)
def get_city_center(city: str, state: str = "FL"):
    if not city or not str(city).strip():
        return None, None

    try:
        location = geolocator.geocode(f"{city}, {state}", timeout=10)
        if location:
            return location.latitude, location.longitude
    except Exception:
        pass

    return None, None


def parse_sidebar_inputs(niches_input: str, cities_input: str):
    niches = [line.strip() for line in niches_input.splitlines() if line.strip()]
    cities = [line.strip() for line in cities_input.splitlines() if line.strip()]

    if not niches:
        raise ValueError("Please enter at least one niche.")
    if not cities:
        raise ValueError("Please enter at least one city.")

    search_pairs = [(niche, city) for niche in niches for city in cities]
    return niches, cities, search_pairs


def _lead_identity(lead: dict) -> str:
    place_id = str(lead.get("place_id") or "").strip()
    if place_id:
        return f"place:{place_id}"

    name = str(lead.get("name") or "").strip().lower()
    address = str(lead.get("address") or "").strip().lower()
    city = str(lead.get("search_city") or "").strip().lower()
    return f"name_addr_city:{name}|{address}|{city}"


def _dedupe_discovered_leads(raw_leads_all: list[dict]) -> list[dict]:
    deduped = []
    seen_keys = set()

    for lead in raw_leads_all:
        dedupe_key = _lead_identity(lead)
        if dedupe_key in seen_keys:
            continue
        seen_keys.add(dedupe_key)
        deduped.append(lead)

    return deduped


def _build_filtered_views(ready_leads: list[dict], min_score: int):
    df = pd.DataFrame(ready_leads)
    if df.empty:
        return df, df.copy(), df.copy()

    if "score" in df.columns:
        df["score_numeric"] = pd.to_numeric(df["score"], errors="coerce")
        filtered_df = df[df["score_numeric"] >= min_score].copy()
        filtered_df = filtered_df.sort_values(by="score_numeric", ascending=False)
    else:
        filtered_df = df.copy()

    if "email" not in filtered_df.columns:
        filtered_df["email"] = ""

    outreach_df = filtered_df[
        filtered_df["email"].fillna("").astype(str).str.strip() != ""
    ].copy()
    other_scored_df = filtered_df[
        filtered_df["email"].fillna("").astype(str).str.strip() == ""
    ].copy()

    return filtered_df, outreach_df, other_scored_df


def execute_full_pipeline(
    niches: list[str],
    cities: list[str],
    max_results: int,
    outreach_limit: int,
    min_score: int,
    progress_bar,
    status_text,
):
    data_dir = PROJECT_ROOT / "data"
    data_dir.mkdir(exist_ok=True)

    raw_path = data_dir / "leads_raw.csv"
    enriched_path = data_dir / "leads_enriched.csv"
    scored_path = data_dir / "leads_scored.csv"
    outreach_path = data_dir / "leads_outreach.csv"
    ready_path = data_dir / "leads_ready.csv"

    search_pairs = [(niche, city) for niche in niches for city in cities]
    timings = {}

    status_text.info("Step 1/5: Discovering leads across niches and cities...")
    t0 = time.perf_counter()
    raw_leads_all = []

    for niche_value, city_value in search_pairs:
        combo_query = f"{niche_value} in {city_value}"
        combo_results = search_google_maps(
            query=combo_query,
            max_results=int(max_results),
        )
        for lead in combo_results:
            enriched_lead = dict(lead)
            enriched_lead["search_niche"] = niche_value
            enriched_lead["search_city"] = city_value
            enriched_lead["search_query"] = combo_query
            raw_leads_all.append(enriched_lead)

    raw_leads = _dedupe_discovered_leads(raw_leads_all)
    save_leads_to_csv(raw_leads, str(raw_path))
    timings["Lead Discovery"] = time.perf_counter() - t0
    progress_bar.progress(20)

    status_text.info("Step 2/5: Enriching websites...")
    t0 = time.perf_counter()
    enriched_leads = enrich_leads(
        raw_leads,
        limit=len(raw_leads),
        sleep_seconds=0.0,
        max_workers=6,
    )
    pd.DataFrame(enriched_leads).to_csv(enriched_path, index=False)
    timings["Website Enrichment"] = time.perf_counter() - t0
    progress_bar.progress(40)

    status_text.info("Step 3/5: Scoring leads with AI...")
    t0 = time.perf_counter()
    scored_leads = score_leads(
        enriched_leads,
        limit=len(enriched_leads),
        sleep_seconds=0.0,
        max_workers=6,
    )
    pd.DataFrame(scored_leads).to_csv(scored_path, index=False)
    timings["AI Scoring"] = time.perf_counter() - t0
    progress_bar.progress(60)

    status_text.info("Step 4/5: Generating outreach...")
    t0 = time.perf_counter()
    scored_sorted = sorted(
        scored_leads,
        key=lambda row: pd.to_numeric(row.get("score"), errors="coerce")
        if pd.notna(pd.to_numeric(row.get("score"), errors="coerce"))
        else -1,
        reverse=True,
    )
    top_for_outreach = scored_sorted[: int(outreach_limit)]

    outreach_subset = generate_outreach_batch(
        top_for_outreach,
        limit=len(top_for_outreach),
        sleep_seconds=0.0,
        max_workers=6,
    )

    outreach_by_key = {_lead_identity(lead): lead for lead in outreach_subset}
    merged_leads = []
    for lead in scored_leads:
        key = _lead_identity(lead)
        if key in outreach_by_key:
            merged_leads.append(outreach_by_key[key])
        else:
            base = dict(lead)
            base.update(
                {
                    "subject": "",
                    "email": "",
                    "cta": "",
                    "followup_1": "",
                    "followup_2": "",
                    "outreach_error": None,
                }
            )
            merged_leads.append(base)

    pd.DataFrame(merged_leads).to_csv(outreach_path, index=False)
    timings["Outreach Generation"] = time.perf_counter() - t0
    progress_bar.progress(80)

    status_text.info("Step 5/5: Running contactability review...")
    t0 = time.perf_counter()
    ready_leads = evaluate_contactability_batch(merged_leads)
    pd.DataFrame(ready_leads).to_csv(ready_path, index=False)
    timings["Contactability Review"] = time.perf_counter() - t0
    progress_bar.progress(100)

    ready_count = sum(1 for row in ready_leads if row.get("contactability_status") == "ready")
    review_count = sum(
        1 for row in ready_leads if row.get("contactability_status") == "review"
    )

    filtered_df, outreach_df, other_scored_df = _build_filtered_views(
        ready_leads=ready_leads,
        min_score=int(min_score),
    )
    map_df = filtered_df.copy()

    return {
        "raw_leads": raw_leads,
        "enriched_leads": enriched_leads,
        "scored_leads": scored_leads,
        "ready_leads": ready_leads,
        "filtered_df": filtered_df,
        "map_df": map_df,
        "outreach_df": outreach_df,
        "other_scored_df": other_scored_df,
        "outreach_subset": outreach_subset,
        "ready_count": ready_count,
        "review_count": review_count,
        "timings": timings,
        "search_pairs": search_pairs,
        "niches": niches,
        "cities": cities,
        "status_message": (
            f"Pipeline complete for {len(search_pairs)} search combination(s) "
            f"across {len(niches)} niche(s) and {len(cities)} city(s)."
        ),
        "output_paths": {
            "raw": str(raw_path),
            "enriched": str(enriched_path),
            "scored": str(scored_path),
            "outreach": str(outreach_path),
            "ready": str(ready_path),
        },
        "run_settings": {
            "max_results": int(max_results),
            "outreach_limit": int(outreach_limit),
            "min_score": int(min_score),
        },
    }


def _ensure_dataframe(value):
    if isinstance(value, pd.DataFrame):
        return value.copy()
    if value is None:
        return pd.DataFrame()
    return pd.DataFrame(value)


def prepare_map_dataframe(filtered_df: pd.DataFrame) -> pd.DataFrame:
    map_df = _ensure_dataframe(filtered_df)

    if "search_city" not in map_df.columns:
        map_df["search_city"] = ""
    if "latitude" not in map_df.columns:
        map_df["latitude"] = None
    if "longitude" not in map_df.columns:
        map_df["longitude"] = None
    if "geocode_quality" not in map_df.columns:
        map_df["geocode_quality"] = None

    if map_df.empty:
        return map_df

    # ORIGINAL GEOCODING LOOP (the version that worked before)
    missing_coords_mask = map_df["latitude"].isna() | map_df["longitude"].isna()

    for idx, row in map_df.loc[missing_coords_mask].iterrows():
        lat, lon, quality = geocode_address_with_fallback(
            row.get("address", ""),
            city=row.get("search_city", ""),
            state="FL",
        )

        map_df.at[idx, "latitude"] = lat
        map_df.at[idx, "longitude"] = lon
        map_df.at[idx, "geocode_quality"] = quality

    quality_empty_mask = map_df["geocode_quality"].isna() | (
        map_df["geocode_quality"].astype(str).str.strip() == ""
    )

    provided_coords_mask = map_df["latitude"].notna() & map_df["longitude"].notna()

    map_df.loc[quality_empty_mask & provided_coords_mask, "geocode_quality"] = "provided"

    return map_df


def _score_badge_color(score) -> str:
    numeric_score = pd.to_numeric(score, errors="coerce")
    if pd.notna(numeric_score) and numeric_score >= 7:
        return "#22c55e"
    if pd.notna(numeric_score) and numeric_score >= 5:
        return "#3b82f6"
    return "#f59e0b"


def _status_badge_color(status: str) -> str:
    return "#16a34a" if str(status).lower() == "ready" else "#f59e0b"


def _render_map_section(prepared_map_df: pd.DataFrame):
    st.markdown("---")
    st.subheader("Lead Map")

    available_map_cities = sorted(
        {
            str(value).strip()
            for value in prepared_map_df.get("search_city", pd.Series(dtype=str)).dropna().tolist()
            if str(value).strip()
        }
    )
    map_options = ["All Cities"] + available_map_cities

    if st.session_state.get("map_city_selection") not in map_options:
        st.session_state.map_city_selection = "All Cities"

    st.selectbox(
        "Jump to city",
        options=map_options,
        key="map_city_selection",
    )
    map_city_selection = st.session_state.map_city_selection

    if available_map_cities:
        st.caption("Available cities on map: " + ", ".join(available_map_cities))

    # Start from persisted map rows, then geocode missing coordinates on demand.
    map_df = prepared_map_df.copy()
    if map_city_selection != "All Cities" and "search_city" in map_df.columns:
        map_df = map_df[
            map_df["search_city"].fillna("").astype(str).str.strip() == map_city_selection
        ].copy()

    if "search_city" not in map_df.columns:
        map_df["search_city"] = ""
    if "latitude" not in map_df.columns:
        map_df["latitude"] = None
    if "longitude" not in map_df.columns:
        map_df["longitude"] = None
    if "geocode_quality" not in map_df.columns:
        map_df["geocode_quality"] = None

    # Keep geocoding in map render path (previous working behavior).
    missing_coords_mask = map_df["latitude"].isna() | map_df["longitude"].isna()

    for idx, row in map_df.loc[missing_coords_mask].head(10).iterrows():
        lat, lon, quality = geocode_address_with_fallback(
            row.get("address", ""),
            city=row.get("search_city", ""),
            state="FL",
        )
        map_df.at[idx, "latitude"] = lat
        map_df.at[idx, "longitude"] = lon
        map_df.at[idx, "geocode_quality"] = quality

        plotted_df = map_df.dropna(subset=["latitude", "longitude"]).copy()

    if plotted_df.empty:
        st.info(
            f"Map coordinates could not be generated for the current filtered leads"
            + (f" in {map_city_selection}." if map_city_selection != "All Cities" else ".")
        )
        return

    plotted_df["tooltip_name"] = plotted_df["name"].fillna("Unknown Lead")
    plotted_df["tooltip_score"] = plotted_df["score"].fillna("N/A")
    plotted_df["tooltip_status"] = plotted_df["contactability_status"].fillna("unknown")
    plotted_df["tooltip_address"] = plotted_df["address"].fillna("")
    plotted_df["tooltip_geocode_quality"] = plotted_df["geocode_quality"].fillna("unknown")

    def marker_color(row):
        score = pd.to_numeric(row.get("score"), errors="coerce")
        status = str(row.get("contactability_status", "")).lower()

        if status == "review":
            return [245, 158, 11, 220]
        if pd.notna(score) and score >= 7:
            return [34, 197, 94, 220]
        if pd.notna(score) and score >= 5:
            return [59, 130, 246, 220]
        return [156, 163, 175, 220]

    plotted_df["marker_color"] = plotted_df.apply(marker_color, axis=1)

    coord_counts = {}
    adjusted_lats = []
    adjusted_lons = []

    for _, row in plotted_df.iterrows():
        key = (round(float(row["latitude"]), 5), round(float(row["longitude"]), 5))
        count = coord_counts.get(key, 0)
        lat_offset = 0.00025 * count
        lon_offset = 0.00025 * count
        adjusted_lats.append(float(row["latitude"]) + lat_offset)
        adjusted_lons.append(float(row["longitude"]) + lon_offset)
        coord_counts[key] = count + 1

    plotted_df["plot_latitude"] = adjusted_lats
    plotted_df["plot_longitude"] = adjusted_lons

    center_lat = plotted_df["plot_latitude"].mean()
    center_lon = plotted_df["plot_longitude"].mean()

    layer = pdk.Layer(
        "ScatterplotLayer",
        data=plotted_df,
        get_position="[plot_longitude, plot_latitude]",
        get_radius=220,
        get_fill_color="marker_color",
        pickable=True,
    )

    view_state = pdk.ViewState(
        latitude=center_lat,
        longitude=center_lon,
        zoom=12,
        pitch=0,
    )

    tooltip = {
        "html": """
            <b>{tooltip_name}</b><br/>
            Score: {tooltip_score}<br/>
            Status: {tooltip_status}<br/>
            Geocode: {tooltip_geocode_quality}<br/>
            {tooltip_address}
        """,
        "style": {
            "backgroundColor": "rgba(15, 23, 42, 0.95)",
            "color": "white",
            "fontSize": "13px",
            "padding": "10px",
            "borderRadius": "8px",
        },
    }

    deck = pdk.Deck(
        layers=[layer],
        initial_view_state=view_state,
        tooltip=tooltip,
        map_style="dark",
    )
    st.pydeck_chart(deck, use_container_width=True)

    missing_count = len(map_df) - len(plotted_df)
    if missing_count > 0:
        st.caption(f"{missing_count} lead(s) could not be mapped because coordinates were not found.")


def render_pipeline_results(results):
    raw_leads = results.get("raw_leads", [])
    scored_leads = results.get("scored_leads", [])
    outreach_subset = results.get("outreach_subset", [])
    ready_count = results.get("ready_count", 0)
    review_count = results.get("review_count", 0)
    timings = results.get("timings", {})
    search_pairs = results.get("search_pairs", [])
    filtered_df = _ensure_dataframe(results.get("filtered_df"))
    map_df = _ensure_dataframe(results.get("map_df"))
    if map_df.empty:
        map_df = filtered_df.copy()

    st.subheader("Pipeline Results")
    m1, m2, m3, m4, m5, m6 = st.columns(6)
    m1.metric("Search Combos", len(search_pairs))
    m2.metric("Raw", len(raw_leads))
    m3.metric("Scored", len(scored_leads))
    m4.metric("Outreach Generated", f"{len(outreach_subset)} / {len(scored_leads)}")
    m5.metric("Ready", ready_count)
    m6.metric("Review", review_count)

    st.markdown("---")
    st.subheader("Stage Timings")
    timing_df = pd.DataFrame(
        [{"Stage": stage, "Seconds": round(seconds, 2)} for stage, seconds in timings.items()]
    )
    st.dataframe(timing_df, use_container_width=True, hide_index=True)

    _render_map_section(map_df)


